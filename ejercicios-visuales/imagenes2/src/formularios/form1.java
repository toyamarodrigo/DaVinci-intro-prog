    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar apuesta");
        } else {
            d = random.nextInt(6) + 1;
            jLabel1.setIcon(new ImageIcon(getClass().getResource("/img/dado" + d + ".png")));
            ap = Integer.parseInt(jTextField2.getText());

            if (d == 6) {
                ap = ap * 2;
            } else {
                ap = 0;
            }
        }

        jTextField3.setText("" + ap);
        jTextField2.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

